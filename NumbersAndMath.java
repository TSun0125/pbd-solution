public class NumbersAndMath {
	public static void main(String[] args){
		System.out.println("Now I am going to count my pens.");
		System.out.println("I think I have 15 pens in total.");
		System.out.println("Black Pen "+(3+6/2));
		System.out.println("Blue Pen "+(15-9));
		System.out.println("Oh I have "+(10-9)+" Red Pen.");

		System.out.println("How many pens do I have now?");
		System.out.println("Oh, I have 2 more pens I think.");
		System.out.println("So where's the rest of my pens?");

		System.out.println("Ahh, I have a marker and a highlighter, too.");

		System.out.println("Marker "+(2*1/2));
		System.out.println("Highlighter "+((15-6-6-1)/2));
		System.out.println("So I really have 15 pens in total.");
	}
}